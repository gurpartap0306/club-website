<h1 align="center">Juventus The Old Lady</h1>
<p>Juventus Football Club S.p.A. colloquially known as Juve is a professional Italian football club in Turin, Piedmont. Founded in 1897 by a group of Torinese
 students, the club has worn a black and white striped home kit since 1903 and has played home matches in different grounds around its city, the latest being 
 the 41,507-capacity Allianz Stadium. Nicknamed Vecchia Signora ("the Old Lady"), the club has won thirty-three official league titles, twelve Coppa Italia 
 titles and seven national Super Cups titles, being the record holder for all these competitions; two Intercontinental Cups, two European Champion Clubs' Cup
 and UEFA Champions Leagues, one European Cup Winners' Cup, a national record of three UEFA Cups, two UEFA Super Cups and one UEFA Intertoto Cup.
 Consequently, the side leads the historical Federazione Italiana Giuoco Calcio (FIGC) ranking whilst on the international stage occupies the 4th position in 
 Europe and the eight in the world for most confederation titles won with eleven trophies, having led the UEFA rankings during seven seasons since its 
 inception in 1979, the most for an Italian team.
</p>
<br>
<h3>History</h3>
<p>
Founded with the name of Sport-Club Juventus, initially as an athletics club, it is the second oldest of its kind still active in the country after Genoa's 
football section (1893) and has competed uninterruptedly in the top flight league (reformulated as Serie A from 1929) since its debut in 1900 after changing 
its name to Foot-Ball Club Juventus, with the exception of the 2006–07 season, being managed by the industrial Agnelli family almost continuously since 1923.
 The relationship between the club and that dynasty is the oldest and longest in national sports, making Juventus the first professional sporting club in the 
 country, having established itself as a major force in the national stage since the 1930s and at confederation level since the mid-1970s and becoming one of 
 the first ten wealthiest in world football in terms of value, revenue and profit since the mid-1990s,being stocked in Borsa italiana since 2001.</p>
<br><h3>Dominance</h3>
 <p>FIAT owner Edoardo Agnelli gained control of the club in 1923 and built a new stadium.This helped the club to its second scudetto (league championship)
 in the 1925–26 season, after beating Alba Roma with an aggregate score of 12–1 (Antonio Vojak's goals were essential that season). The club established 
 itself as a major force in Italian football since the 1930s, becoming the country's first professional club and the first with a decentralised fan base,
 which led it to win a record of five consecutive Italian championships (the first four under the management of Carlo Carcano) and form the core of the Italy 
 national team during the Vittorio Pozzo's era, including the 1934 world champion squad,with star players such as Raimundo Orsi, Luigi Bertolini. 
 Giovanni Ferrari and Luis Monti, among others.
</p>
